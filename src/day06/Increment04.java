package day06;

public class Increment04 {

	public static void main(String[] args) {
		// int data tipinde bır tane variabl olustur.
		// bu variable i her seferinde 1 arttırmak icin 3 yontemi kullan
		
		int a = 10;
		a = a+1;
		System.out.println(a);
		
		a +=1;
		System.out.println(a);

		a ++;
		System.out.println(a);
		
		
	}

}
